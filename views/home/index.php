    <h1>Demo app</h1>
    <p>Deze app heeft alle features geïmplementeerd die de uiteindelijk app ook moet hebben,
        zij het in een sterk vereenvoudigde (en dus per definitie voor werkdoleeeinden onbruikbare) vorm.
        Volgende features werden geïmplementeerd:</p>
    <ul>
        <li>Herstellingen beheren met behulp van een ingebouwde QR scanner</li>
        <li>Inventaris uitvoeren met behulp van een QR scanner</li>
        <li>Inventaris uitvoeren met meerdere personen tegelijktertijd</li>
        <li>Opslaan van een duiklocatie</li>
        <li>Een timer met een geluid waarbij de druk ingegeven moet worden</li>
        <li>Materiaal inscannen net vóór de start van een duikoefening</li>
        <li>Defecten van materiaal m.b.v. de scanner doorgeven net na een duikoefening</li>
        <li>Tonen van een map die aangeeft waar duikoefeningen werden gehouden</li>
        <li>Door te klikken op een locatie op de map onmiddellijk de details van deze duikoefening te zien</li>
        <li>Afhankelijk van wie er inlogt, wordt een bepaald inhoud en startpagina getoond, en andere inhoud afgeschermd</li>
        <li>De app toont mooi op een smartphone en een tablet</li>
        <li>Alle functionaliteit zoals de QR scanner werkt ook op een Android smartphone en een Android tablet</li>
        <li>De app is voldoende intuïtief in gebruik, zodat werknemers het gebruik ervan in geen tijd onder de knie zullen hebben</li>
        <li>App kan geïnstallerd worden</li>
    </ul>
